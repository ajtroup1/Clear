func TestFunctionStatements(t *testing.T) {
	tests := []struct {
		input          string
		expectedName   *ast.Identifier
		expectedParams []string
		expectedBody   string
	}{
		{"fn x() {}", &ast.Identifier{Value: "x"}, []string{}, ""},
		{"fn add(x, y) { x + y }", &ast.Identifier{Value: "add"}, []string{"x", "y"}, "(x + y)"},
	}

	for _, tt := range tests {
		l := lexer.New(tt.input)
		p := New(l)
		program := p.ParseProgram()
		checkParserErrors(t, p)

		if len(program.Statements) != 1 {
			t.Fatalf("program.Statements does not contain 1 statements. got=%d",
				len(program.Statements))
		}

		stmt := program.Statements[0]
		functionStmt, ok := stmt.(*ast.FunctionStatement)
		if !ok {
			t.Fatalf("stmt not *ast.FunctionStatement. got=%T", stmt)
		}

		if functionStmt.TokenLiteral() != "fn" {
			t.Fatalf("functionStmt.TokenLiteral not 'fn', got %q",
				functionStmt.TokenLiteral())
		}

		if functionStmt.Name.Value != tt.expectedName.Value {
			t.Errorf("functionStmt.Name.Value not %s. got=%s",
				tt.expectedName.Value, functionStmt.Name.Value)
		}

		if len(functionStmt.Parameters) != len(tt.expectedParams) {
			t.Fatalf("functionStmt.Parameters has not enough parameters. got=%d",
				len(functionStmt.Parameters))
		}

		for i, ident := range tt.expectedParams {
			if functionStmt.Parameters[i].Value != ident {
				t.Errorf("parameter is not %s. got=%s", ident, functionStmt.Parameters[i].Value)
			}
		}

		if functionStmt.Body.String() != tt.expectedBody {
			t.Errorf("functionStmt.Body.String() not %s. got=%s",
				tt.expectedBody, functionStmt.Body.String())
		}
	}
}

